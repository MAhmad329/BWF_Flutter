void main() {
  // Integer
  int age = 25;

  // Double
  double height = 5.9;

  // String
  String name = "John Doe";

  // Boolean
  bool isStudent = true;

  // List
  List<String> colors = ["Red", "Green", "Blue"];

  // Map
  Map<String, int> scores = {"Muhammad": 90, "Ahmad": 85};

  // Dynamic
  dynamic anything = "Hello";
  anything = 123;

  print(age);
  print(height);
  print(name);
  print(isStudent);
  print(colors);
  print(scores);
  print(anything);
}
